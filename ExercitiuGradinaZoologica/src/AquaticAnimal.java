public class AquaticAnimal extends Animal{

    protected int deep ;

    public AquaticAnimal(String color,int memberNumber,int deep,String specimen){
        this.color=color;
        this.memberNumber=memberNumber;
        this.deep=deep;
        this.specimen=specimen;

    }

    public AquaticAnimal() {
    }

    public int getDeep(){
        return deep;

    }











}


